function Sum(num1, num2) {
    return num1 + num2
}

function Rest(num1, num2) {
    return num1 - num2
}

function Multi(num1, num2) {
    return num1 * num2
}

function Divide(num1, num2) {
    return num1 / num2
}

exports.sum = Sum
exports.rest = Rest 
exports.multi = Multi
exports.divide = Divide